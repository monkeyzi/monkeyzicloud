package com.gaoyg.monkeyzicloud.provider.ucloud.enums;


/**
 * The enum Uac menu status enum.
 *
 */
public enum UcloudMenuStatusEnum {
	/**
	 * 启用
	 */
	ENABLE("ENABLE", "启用"),
	/**
	 * 禁用
	 */
	DISABLE("DISABLE", "禁用");

	/**
	 * The Type.
	 */
	String type;
	/**
	 * The Name.
	 */
	String name;

	UcloudMenuStatusEnum(String type, String name) {
		this.type = type;
		this.name = name;
	}

	/**
	 * Gets type.
	 *
	 * @return the type
	 */
	public String getType() {
		return type;
	}

	/**
	 * Gets name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}


	/**
	 * Gets name.
	 *
	 * @param type the type
	 *
	 * @return the name
	 */
	public static String getName(String type) {
		for (UcloudMenuStatusEnum ele : UcloudMenuStatusEnum.values()) {
			if (type.equals(ele.getType())) {
				return ele.getName();
			}
		}
		return null;
	}

	/**
	 * Gets enum.
	 *
	 * @param type the type
	 *
	 * @return the enum
	 */
	public static UcloudMenuStatusEnum getEnum(String type) {
		for (UcloudMenuStatusEnum ele : UcloudMenuStatusEnum.values()) {
			if (type.equals(ele.getType())) {
				return ele;
			}
		}
		return UcloudMenuStatusEnum.ENABLE;
	}
}
